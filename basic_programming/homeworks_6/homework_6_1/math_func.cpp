//math_func.cpp
#include <iostream>
#include <cmath>
#include "math_func.h"

double add(double x, double y) {
	return x+y;
}
double multiply(double x, double y) {
	return x*y;
}
double subtract(double x, double y) {
	return x-y;
}
double divide(double x, double y) {
	return x / y;     
}
double power(double x, double y) {
	return pow(x,y);
}
